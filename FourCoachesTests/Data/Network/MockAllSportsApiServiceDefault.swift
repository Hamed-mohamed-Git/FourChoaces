    func getLeagues(completion: @escaping (Result<FourCoaches.Leagues, Error>) -> Void, sportType: FourCoaches.SportType) {
        getURLTask(request: jsonLeagueRequest, completion: completion)
    }
    
    func getFixtureScores<D>(sportType: FourCoaches.SportType, leagueID: String, completion: @escaping (Result<D, Error>) -> Void) where D : Decodable {
        getURLTask(request:jsonFootballFixtureRequest, completion: completion)
    }
    
    func getLiveScoreScores<D>(sportType: FourCoaches.SportType, leagueID: String, completion: @escaping (Result<D, Error>) -> Void) where D : Decodable {
        getURLTask(request: jsonFootballLivescoreRequest , completion: completion)
    }
    
    func getLeagueTeamsScores(sportType: FourCoaches.SportType, leagueID: String, completion: @escaping (Result<FourCoaches.LeagueTeams, Error>) -> Void) {
        getURLTask(request: jsonTeamReques, completion: completion)
    }
    
    func getTennisPlayerByLeagueId(leagueID: String, completion: @escaping (Result<FourCoaches.TennisPlayers, Error>) -> Void) {
        getURLTask(request: jsonTennisData, completion: completion)
    }
    
    private func getURLTask<T:Decodable>(request:String,completion:@escaping(Result<T,Error>) -> Void) {
        do{
            let encodedData = try JSONDecoder().decode(T.self, from: Data(request.utf8))
            completion(Result.success(encodedData))
        }catch let encodingError{
            completion(Result.failure(encodingError))
        }
    }
    
}



